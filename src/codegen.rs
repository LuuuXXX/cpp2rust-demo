#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::{ClassIR, ExtractedDecls, FunctionIR, ParamIR};

    fn make_fn(name: &str, rust_name: &str, ret: &str, params: Vec<ParamIR>) -> FunctionIR {
        let param_types: Vec<String> = params.iter().map(|p| p.cpp_type.clone()).collect();
        let cpp_sig = format!("{} {}({})", ret, name, param_types.join(", "));
        FunctionIR {
            name: name.to_string(),
            rust_name: rust_name.to_string(),
            return_type: ret.to_string(),
            rust_return_type: crate::ast::cpp_to_rust_type(ret),
            params,
            qualified_name: name.to_string(),
            cpp_signature: cpp_sig,
            is_const: false,
            is_static: false,
            is_virtual: false,
            is_pure: false,
            class_name: None,
        }
    }

    fn int_param(name: &str) -> ParamIR {
        ParamIR {
            name: name.to_string(),
            cpp_type: "int".to_string(),
            rust_type: "i32".to_string(),
        }
    }

    #[test]
    fn render_ffi_basic() {
        let decls = ExtractedDecls {
            functions: vec![make_fn(
                "add",
                "add",
                "int",
                vec![int_param("a"), int_param("b")],
            )],
            classes: vec![],
        };
        let src = render_ffi(&decls, "mylib", "mylib.hpp");
        assert!(src.contains("import_lib!"));
        assert!(src.contains(r#"link_name = "mylib""#));
        assert!(src.contains("fn add"));
        assert!(src.contains("a: i32"));
        assert!(src.contains("-> i32"));
    }

    #[test]
    fn render_ffi_void_return() {
        let decls = ExtractedDecls {
            functions: vec![make_fn("process", "process", "void", vec![int_param("v")])],
            classes: vec![],
        };
        let src = render_ffi(&decls, "mylib", "mylib.hpp");
        // void return should not produce `-> ()`.
        assert!(!src.contains("-> ()"));
        assert!(src.contains("fn process(v: i32)"));
    }

    #[test]
    fn render_cargo_toml_contains_hicc() {
        let toml = render_cargo_toml("my-ffi", "mylib");
        assert!(toml.contains("hicc"));
        assert!(toml.contains("hicc-build"));
        assert!(toml.contains("my-ffi"));
    }

    #[test]
    fn render_class_method() {
        let method = FunctionIR {
            name: "update".to_string(),
            rust_name: "update".to_string(),
            return_type: "void".to_string(),
            rust_return_type: "()".to_string(),
            params: vec![
                ParamIR {
                    name: "x".to_string(),
                    cpp_type: "double".to_string(),
                    rust_type: "f64".to_string(),
                },
                ParamIR {
                    name: "y".to_string(),
                    cpp_type: "double".to_string(),
                    rust_type: "f64".to_string(),
                },
            ],
            qualified_name: "Widget::update".to_string(),
            cpp_signature: "void Widget::update(double, double)".to_string(),
            is_const: false,
            is_static: false,
            is_virtual: false,
            is_pure: false,
            class_name: Some("Widget".to_string()),
        };
        let class = ClassIR {
            name: "Widget".to_string(),
            qualified_name: "Widget".to_string(),
            methods: vec![method],
        };
        let decls = ExtractedDecls {
            functions: vec![],
            classes: vec![class],
        };
        let src = render_ffi(&decls, "mywidget", "widget.hpp");
        assert!(src.contains("import_class!"));
        assert!(src.contains(r#"cpp(class = "Widget")"#));
        assert!(src.contains("fn update(&mut self, x: f64, y: f64)"));
        assert!(src.contains("class Widget;"));
    }
}
