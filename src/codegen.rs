    fn make_fn(name: &str, rust_name: &str, ret: &str, params: Vec<ParamIR>) -> FunctionIR {
        let param_types: Vec<String> = params.iter().map(|p| p.cpp_type.clone()).collect();
        let cpp_sig = format!("{} {}({})", ret, name, param_types.join(", "));
        FunctionIR {
            name: name.to_string(),
            rust_name: rust_name.to_string(),
            return_type: ret.to_string(),
            rust_return_type: crate::ast::cpp_to_rust_type(ret),
            params,
            qualified_name: name.to_string(),
            cpp_signature: cpp_sig,
            is_const: false,
            is_static: false,
            is_virtual: false,
            is_pure: false,
            class_name: None,
        }
    }

    fn int_param(name: &str) -> ParamIR {
        ParamIR {
            name: name.to_string(),
            cpp_type: "int".to_string(),
            rust_type: "i32".to_string(),
        }
    }

