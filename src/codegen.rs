fn render_import_class(out: &mut String, class: &ClassIR) {
    // Only include instance methods (non-static) in import_class!
    let instance_methods: Vec<&FunctionIR> =
        class.methods.iter().filter(|m| !m.is_static).collect();

    if instance_methods.is_empty() {
        // Don't emit an empty import_class! block; the class will still be
        // forward-declared in import_lib! (if it is not abstract).
        return;
    }

    writeln!(out, "hicc::import_class! {{").unwrap();
    if class.is_abstract {
        // Fully-abstract C++ class: map to a hicc `#[interface]` trait.
        // hicc uses #[interface] instead of #[cpp(class = "...")] for pure-virtual
        // interfaces; the generated Rust trait can then be used as a bound.
        writeln!(out, "    #[interface]").unwrap();
        writeln!(out, "    class {} {{", class.name).unwrap();
    } else {
        writeln!(out, "    #[cpp(class = \"{}\")]", class.qualified_name).unwrap();
        writeln!(out, "    class {} {{", class.name).unwrap();
    }

    for method in &instance_methods {
        render_method(out, method);
    }

    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
}

fn render_string_slice(name: &str, values: &[&str]) -> String {
    let mut out = String::new();
    writeln!(out, "pub const {}: &[&str] = &[", name).unwrap();
    for value in values {
        writeln!(out, "    {:?},", value).unwrap();
    }
    writeln!(out, "];").unwrap();
    out
}

fn render_usize_slice(name: &str, values: &[usize]) -> String {
    let mut out = String::new();
    writeln!(out, "pub const {}: &[usize] = &[", name).unwrap();
    for value in values {
        writeln!(out, "    {},", value).unwrap();
    }
    writeln!(out, "];").unwrap();
    out
}

fn render_pair_slice(name: &str, values: &[(&str, &str)]) -> String {
    let mut out = String::new();
    writeln!(out, "pub const {}: &[(&str, &str)] = &[", name).unwrap();
    for (left, right) in values {
        writeln!(out, "    ({:?}, {:?}),", left, right).unwrap();
    }
    writeln!(out, "];").unwrap();
    out
}

fn render_method(out: &mut String, func: &FunctionIR) {
    // Build the method signature for #[cpp(method = "...")]
    // The signature is relative to the class, so we use the bare method
    // signature without the class name prefix.
    let param_types: Vec<&str> = func.params.iter().map(|p| p.cpp_type.as_str()).collect();
    let const_suffix = if func.is_const { " const" } else { "" };
    let method_sig = format!(
        "{} {}({}){}",
        func.return_type,
        func.name,
        param_types.join(", "),
        const_suffix
    );

    writeln!(out, "        #[cpp(method = \"{method_sig}\")]").unwrap();

    // Build the Rust method signature.
    let self_arg = if func.is_static {
        // Static methods don't take self.
        String::new()
    } else if func.is_const {
        "&self, ".to_string()
    } else {
        "&mut self, ".to_string()
    };

    let rust_params = render_rust_params(&func.params, &self_arg);
    let ret = render_return_type(&func.rust_return_type);

    writeln!(out, "        fn {}{}{};", func.rust_name, rust_params, ret).unwrap();
}

fn render_import_lib(out: &mut String, decls: &ExtractedDecls, link_name: &str) {
    writeln!(out, "hicc::import_lib! {{").unwrap();
    writeln!(out, "    #![link_name = \"{link_name}\"]").unwrap();
    writeln!(out).unwrap();

    // Forward-declare every concrete class used so hicc knows they are C++ types.
    // Abstract classes (mapped to #[interface] traits) are not forward-declared
    // here: they are not concrete value types in hicc.
    for class in &decls.classes {
        if !class.is_abstract {
            writeln!(out, "    class {};", class.name).unwrap();
        }
    }

    // Collect all static methods as free functions.
    let static_methods: Vec<&FunctionIR> = decls
        .classes
        .iter()
        .flat_map(|c| c.methods.iter())
        .filter(|m| m.is_static)
        .collect();

    let has_content = !decls.functions.is_empty() || !static_methods.is_empty();
    if has_content && !decls.classes.is_empty() {
        writeln!(out).unwrap();
    }

    // Free functions.
    for func in &decls.functions {
        render_free_function(out, func);
        writeln!(out).unwrap();
    }

    // Static class methods exposed as free functions.
    for func in static_methods {
        // Use class_name::rust_name as the Rust fn name.
        let prefixed = if let Some(ref cls) = func.class_name {
            format!("{}_{}", crate::ast::to_snake_case(cls), func.rust_name)
        } else {
            func.rust_name.clone()
        };
        render_free_function_with_name(out, func, &prefixed);
        writeln!(out).unwrap();
    }

    writeln!(out, "}}").unwrap();
}

fn render_free_function(out: &mut String, func: &FunctionIR) {
    render_free_function_with_name(out, func, &func.rust_name);
}

fn render_free_function_with_name(out: &mut String, func: &FunctionIR, rust_name: &str) {
    writeln!(out, "    #[cpp(func = \"{}\")]", func.cpp_signature).unwrap();
    let rust_params = render_rust_params(&func.params, "");
    let ret = render_return_type(&func.rust_return_type);
    writeln!(out, "    fn {}{}{};", rust_name, rust_params, ret).unwrap();
}

fn render_rust_params(params: &[crate::ast::ParamIR], self_prefix: &str) -> String {
    let param_strs: Vec<String> = params
        .iter()
        .map(|p| format!("{}: {}", sanitize_ident(&p.name), p.rust_type))
        .collect();

    let inner = if self_prefix.is_empty() {
        param_strs.join(", ")
    } else if param_strs.is_empty() {
        // Strip the trailing ", " from self_prefix (e.g. "&self, " → "&self").
        self_prefix.trim_end_matches(", ").to_string()
    } else {
        format!("{}{}", self_prefix, param_strs.join(", "))
    };
    format!("({})", inner)
}

fn render_return_type(rust_type: &str) -> String {
    if rust_type == "()" {
        String::new()
    } else {
        format!(" -> {}", rust_type)
    }
}

/// Ensure a parameter name is a valid Rust identifier (not a keyword).
fn sanitize_ident(name: &str) -> &str {
    match name {
        "type" | "impl" | "mod" | "use" | "pub" | "fn" | "let" | "mut" | "ref" | "move"
        | "loop" | "match" | "if" | "else" | "return" | "break" | "continue" | "where" | "for"
        | "while" | "in" | "as" | "crate" | "self" | "super" | "trait" | "struct" | "enum"
        | "union" | "extern" | "unsafe" | "async" | "await" | "dyn" | "box" | "abstract"
        | "become" | "final" | "override" | "priv" | "typeof" | "unsized" | "virtual" | "yield"
        | "do" | "try" | "macro" => "value",
        other => other,
    }
}

