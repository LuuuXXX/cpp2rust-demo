//! Code generation: turn `ExtractedDecls` into hicc FFI Rust source code.
//!
//! The output consists of:
//!
//! 1. **`hicc::import_class!` blocks** – one per C++ class, mapping public
//!    methods to Rust fn signatures with `#[cpp(method = "...")]`.
//! 2. **`hicc::import_lib!` block** – a single block containing:
//!    - `class Foo;` forward declarations for every referenced class, and
//!    - `#[cpp(func = "...")]` fn items for all free functions (and static
//!      methods, which hicc exposes as free functions).
//! 3. A stub **`build.rs`** for the generated crate that uses `hicc-build` to
//!    compile the C++ adapter code.
//! 4. A minimal **`Cargo.toml`** for the generated crate.

use crate::ast::{ClassIR, ExtractedDecls, FunctionIR, OperatorShimIR, SkippedDecl, SkipCategory};
use std::fmt::Write as _;

// ---------------------------------------------------------------------------
// Public entry-point
// ---------------------------------------------------------------------------

/// Render the full `ffi.rs` content for a feature.
///
/// `source_file_path` is the absolute path to the selected middleware file.
/// The generated file includes a `hicc::cpp!` block that `#include`s only the
/// basename, so its parent directory must be added to the compiler include path via
/// `build.rs` (see [`render_build_rs`]).
#[allow(dead_code)]
pub fn render_ffi(decls: &ExtractedDecls, link_name: &str, source_file_path: &str) -> String {
    let mut out = String::new();

    writeln!(out, "// Auto-generated by cpp2rust-demo.").unwrap();
    writeln!(out, "// Source file : {}", source_file_path).unwrap();
    writeln!(out, "// Link library   : {}", link_name).unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// DO NOT EDIT — run `cpp2rust-demo merge` to consolidate."
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#![allow(non_snake_case, dead_code)]").unwrap();
    writeln!(out).unwrap();

    out.push_str(&render_include_module(source_file_path));
    let class_part = render_class_module(decls);
    if !class_part.is_empty() {
        out.push_str(&class_part);
        out.push('\n');
    }
    out.push_str(&render_free_module(decls, link_name));

    out
}

pub fn render_include_module(source_file_path: &str) -> String {
    let mut out = String::new();
    let include_basename = std::path::Path::new(source_file_path)
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or(source_file_path);
    writeln!(out, "hicc::cpp! {{").unwrap();
    writeln!(out, "    #include \"{}\"", include_basename).unwrap();
    writeln!(out, "}}").unwrap();
    out
}

pub fn render_class_module(decls: &ExtractedDecls) -> String {
    let mut out = String::new();
    if decls.classes.is_empty() {
        return out;
    }
    writeln!(out, "// Class-level metadata for this middleware group.").unwrap();
    writeln!(
        out,
        "pub const CLASS_COUNT: usize = {};",
        decls.classes.len()
    )
    .unwrap();
    let method_counts: Vec<usize> = decls.classes.iter().map(|c| c.methods.len()).collect();
    let static_method_counts: Vec<usize> = decls
        .classes
        .iter()
        .map(|c| c.methods.iter().filter(|m| m.is_static).count())
        .collect();
    let mut class_methods: Vec<(&str, &str)> = Vec::new();
    let mut class_instance_methods: Vec<(&str, &str)> = Vec::new();
    for class in &decls.classes {
        for method in &class.methods {
            class_methods.push((
                class.qualified_name.as_str(),
                method.qualified_name.as_str(),
            ));
            if !method.is_static {
                class_instance_methods.push((
                    class.qualified_name.as_str(),
                    method.qualified_name.as_str(),
                ));
            }
        }
    }
    out.push_str(&render_string_slice(
        "CLASS_NAMES",
        &decls
            .classes
            .iter()
            .map(|c| c.qualified_name.as_str())
            .collect::<Vec<_>>(),
    ));
    out.push_str(&render_usize_slice("CLASS_METHOD_COUNTS", &method_counts));
    out.push_str(&render_usize_slice(
        "CLASS_STATIC_METHOD_COUNTS",
        &static_method_counts,
    ));
    out.push_str(&render_pair_slice("CLASS_METHODS", &class_methods));
    out.push_str(&render_pair_slice(
        "CLASS_INSTANCE_METHODS",
        &class_instance_methods,
    ));
    out.push_str(
        "\
pub fn class_method_count(class_name: &str) -> Option<usize> {\n\
    CLASS_NAMES\n\
        .iter()\n\
        .position(|name| *name == class_name)\n\
        .map(|idx| CLASS_METHOD_COUNTS[idx])\n\
}\n\
\n\
pub fn class_static_method_count(class_name: &str) -> Option<usize> {\n\
    CLASS_NAMES\n\
        .iter()\n\
        .position(|name| *name == class_name)\n\
        .map(|idx| CLASS_STATIC_METHOD_COUNTS[idx])\n\
}\n\
\n\
pub fn class_methods(class_name: &str) -> Vec<&'static str> {\n\
    CLASS_METHODS\n\
        .iter()\n\
        .filter_map(|(cls, method)| (*cls == class_name).then_some(*method))\n\
        .collect()\n\
}\n\
\n\
pub fn class_instance_methods(class_name: &str) -> Vec<&'static str> {\n\
    CLASS_INSTANCE_METHODS\n\
        .iter()\n\
        .filter_map(|(cls, method)| (*cls == class_name).then_some(*method))\n\
        .collect()\n\
}\n",
    );
    out
}

pub fn render_method_module(decls: &ExtractedDecls) -> String {
    let mut out = String::new();
    for class in &decls.classes {
        // For mixed classes, emit the companion #[interface] trait first.
        if class.has_pure_virtual && !class.pure_virtual_methods.is_empty() {
            render_companion_interface(&mut out, class);
            writeln!(out).unwrap();
        }
        render_import_class(&mut out, class);
        writeln!(out).unwrap();
    }
    out
}

pub fn render_free_module(decls: &ExtractedDecls, link_name: &str) -> String {
    let mut out = String::new();
    // When abstract classes or mixed classes with companion interfaces are
    // present we need @make_proxy support.
    let has_abstract = decls.classes.iter().any(|c| c.is_abstract);
    let has_mixed = decls.classes.iter().any(|c| c.has_pure_virtual);
    if has_abstract || has_mixed {
        writeln!(out, "// @make_proxy support: required for wrapping Rust structs as C++ interfaces.").unwrap();
        writeln!(out, "hicc::cpp! {{").unwrap();
        writeln!(out, "    #include <hicc/std/memory.hpp>").unwrap();
        writeln!(out, "}}").unwrap();
        writeln!(out).unwrap();
    }
    render_import_lib(&mut out, decls, link_name);
    out
}

pub fn render_types_module(decls: &ExtractedDecls) -> String {
    let mut cpp_types = std::collections::BTreeSet::new();
    let mut cpp_rust_type_mappings = std::collections::BTreeMap::new();
    for f in &decls.functions {
        cpp_types.insert(f.return_type.as_str());
        cpp_rust_type_mappings
            .entry(f.return_type.as_str())
            .or_insert(f.rust_return_type.as_str());
        for p in &f.params {
            cpp_types.insert(p.cpp_type.as_str());
            cpp_rust_type_mappings
                .entry(p.cpp_type.as_str())
                .or_insert(p.rust_type.as_str());
        }
    }
    for c in &decls.classes {
        for m in &c.methods {
            cpp_types.insert(m.return_type.as_str());
            cpp_rust_type_mappings
                .entry(m.return_type.as_str())
                .or_insert(m.rust_return_type.as_str());
            for p in &m.params {
                cpp_types.insert(p.cpp_type.as_str());
                cpp_rust_type_mappings
                    .entry(p.cpp_type.as_str())
                    .or_insert(p.rust_type.as_str());
            }
        }
    }
    let values: Vec<&str> = cpp_types.into_iter().collect();
    let mappings: Vec<(&str, &str)> = cpp_rust_type_mappings
        .into_iter()
        .collect::<Vec<(&str, &str)>>();
    let mut out = String::from("// Per-group C++ type inventory extracted from AST.\n");
    out.push_str(&format!(
        "pub const CPP_TYPE_COUNT: usize = {};\n",
        values.len()
    ));
    out.push_str(&render_string_slice("CPP_TYPES", &values));
    out.push_str(&render_pair_slice("CPP_RUST_TYPE_MAPPINGS", &mappings));
    out.push_str(
        "\
pub fn rust_type_for(cpp_type: &str) -> Option<&'static str> {\n\
    CPP_RUST_TYPE_MAPPINGS\n\
        .iter()\n\
        .find_map(|(cpp, rust)| (*cpp == cpp_type).then_some(*rust))\n\
}\n\
\n\
pub fn has_cpp_type(cpp_type: &str) -> bool {\n\
    CPP_TYPES.iter().any(|ty| *ty == cpp_type)\n\
}\n",
    );

    // Emit extracted C++ enum definitions as #[repr(C)] Rust enums.
    if !decls.enums.is_empty() {
        out.push_str("\n// C++ enum / enum class definitions.\n");
        for enum_ir in &decls.enums {
            out.push_str("#[repr(C)]\n");
            out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
            out.push_str(&format!("pub enum {} {{\n", enum_ir.name));
            for variant in &enum_ir.variants {
                if let Some(v) = variant.value {
                    out.push_str(&format!("    {} = {},\n", variant.name, v));
                } else {
                    out.push_str(&format!("    {},\n", variant.name));
                }
            }
            out.push_str("}\n\n");
        }
    }

    // Emit simple typedef / using aliases for supported types.
    if !decls.aliases.is_empty() {
        out.push_str("// C++ typedef / using aliases.\n");
        for alias in &decls.aliases {
            out.push_str(&format!(
                "pub type {} = {};\n",
                alias.name, alias.aliased_rust_type
            ));
        }
        out.push('\n');
    }

    out
}

/// Render a standalone `Cargo.toml` for the generated Rust crate.
pub fn render_cargo_toml(crate_name: &str, link_name: &str) -> String {
    format!(
        r#"[package]
name = "{crate_name}"
version = "0.1.0"
edition = "2021"

[dependencies]
hicc = "0.2.3"

[build-dependencies]
hicc-build = "0.2.1"

# The C++ shared/static library to link against.
# Adjust the search path with [package.metadata] or a custom build.rs.
[package.metadata.cpp2rust]
link_name = "{link_name}"
"#
    )
}

/// Render a `build.rs` for the generated Rust crate.
///
/// * `rust_files` – paths to the `.rs` files that contain hicc macros.
/// * `include_dirs` – directories to add as C++ compiler include paths so
///   that `hicc::cpp!` can find the included middleware file.
pub fn render_build_rs(
    link_name: &str,
    no_link: bool,
    rust_files: &[&str],
    include_dirs: &[&str],
) -> String {
    let file_calls: String = rust_files
        .iter()
        .map(|f| format!("    build.rust_file(\"{}\");\n", f))
        .collect();
    let include_calls: String = include_dirs
        .iter()
        .map(|d| format!("    build.include(\"{}\");\n", d))
        .collect();
    // Build the portability note only when include dirs are present.
    let portability_note = if include_dirs.is_empty() {
        String::new()
    } else {
        String::from(
            "    // NOTE: The include paths above are absolute paths recorded at init time.\n\
             //       If you copy this project to another machine, update them to point\n\
             //       to the captured middleware files on the new machine (or use relative paths).\n",
        )
    };
    let external_link_call = if no_link {
        "    // Header-only / no-link mode: skip linking external C++ target library.\n".to_string()
    } else {
        format!(
            "    // Link the actual C++ library (adjust the search path as needed).\n    println!(\"cargo::rustc-link-lib={link_name}\");\n"
        )
    };
    format!(
        r#"fn main() {{
    let mut build = hicc_build::Build::new();
{file_calls}{include_calls}{portability_note}    build.compile("cpp2rust_adapter");
    println!("cargo::rustc-link-lib=cpp2rust_adapter");
    println!("cargo::rustc-link-lib=stdc++");
{external_link_call}
}}
"#
    )
}

/// Render the top-level `lib.rs` that re-exports the given FFI modules.
pub fn render_lib_rs(modules: &[&str]) -> String {
    let mut out = String::from("// Auto-generated by cpp2rust-demo.\n");
    for module in modules {
        out.push_str(&format!("pub mod {};\n", module));
        out.push_str(&format!("pub use {}::*;\n", module));
    }
    out
}

