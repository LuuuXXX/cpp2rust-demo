fn render_import_class(out: &mut String, class: &ClassIR) {
    // Only include instance methods (non-static) in import_class!
    let instance_methods: Vec<&FunctionIR> =
        class.methods.iter().filter(|m| !m.is_static).collect();

    // Nothing to emit: skip if there are no instance methods, no fields, and it's not abstract.
    if instance_methods.is_empty() && class.fields.is_empty() && !class.is_abstract {
        // Don't emit an empty import_class! block; the class will still be
        // forward-declared in import_lib! (if it is not abstract).
        return;
    }
    if instance_methods.is_empty() && class.fields.is_empty() && class.is_abstract {
        // Abstract class with no methods — still need the #[interface] block.
    }

    // Build the inheritance suffix.
    // For mixed classes, always include the companion interface in the bases list.
    let mut extra_bases: Vec<String> = Vec::new();
    if class.has_pure_virtual && !class.pure_virtual_methods.is_empty() {
        extra_bases.push(format!("{}Interface", class.name));
    }
    let all_bases: Vec<String> = class
        .bases
        .iter()
        .map(|b| b.rsplit("::").next().unwrap_or(b.as_str()).to_string())
        .chain(extra_bases)
        .collect();
    let bases_suffix = if all_bases.is_empty() {
        String::new()
    } else {
        format!(": {}", all_bases.join(", "))
    };

    // The Rust struct name: use canonical_name (alias) for template specialisations.
    let rust_name = class
        .canonical_name
        .as_deref()
        .unwrap_or(class.name.as_str());

    writeln!(out, "hicc::import_class! {{").unwrap();
    if class.is_abstract {
        // Fully-abstract C++ class: map to a hicc `#[interface]` trait.
        // hicc requires #[cpp(class = "...")] even for interfaces so that it
        // can set up the ABI method table; without it, make_proxy fails with
        // "incomplete type" because class_methods_type resolves to void.
        writeln!(out, "    #[cpp(class = \"{}\")]", class.qualified_name).unwrap();
        writeln!(out, "    #[interface]").unwrap();
        writeln!(out, "    class {}{} {{", rust_name, bases_suffix).unwrap();
    } else {
        // Concrete class.
        let primary_ctor = class.ctors.first();
        if let Some(ctor) = primary_ctor {
            writeln!(
                out,
                "    #[cpp(class = \"{}\", ctor = \"{}\")]",
                class.qualified_name, ctor.cpp_signature
            )
            .unwrap();
        } else {
            writeln!(out, "    #[cpp(class = \"{}\")]", class.qualified_name).unwrap();
        }
        writeln!(out, "    class {}{} {{", rust_name, bases_suffix).unwrap();
    }

    for method in &instance_methods {
        render_method(out, method);
    }

    // Instance field accessors via `#[cpp(field = "...")]`.
    // Each field gets a read accessor and (unless const) a mutable accessor.
    // hicc's EXPORT_DATA_IN macro generates `&Self::{field}` so we pass
    // `UnqualifiedClassName::field_name` here.  Via C++'s injected-class-name
    // rule, `Self::ClassName` resolves to `Self` itself, making
    // `&Self::ClassName::field` equivalent to `&Self::field`.  Using the
    // fully-qualified namespace form (e.g. `Ns::Class::field`) would produce
    // `&Self::Ns::Class::field` where `Ns` is looked up as a nested member of
    // `Self` and the C++ compiler rejects it.
    if !class.is_abstract {
        for field in &class.fields {
            let rust_type = &field.rust_type;
            writeln!(out, "        #[cpp(field = \"{}\")]", field.qualified_name).unwrap();
            writeln!(
                out,
                "        fn get_{}(&self) -> &{};",
                field.rust_name, rust_type
            )
            .unwrap();
            if !field.is_const {
                writeln!(out, "        #[cpp(field = \"{}\")]", field.qualified_name).unwrap();
                writeln!(
                    out,
                    "        fn get_{}_mut(&mut self) -> &mut {};",
                    field.rust_name, rust_type
                )
                .unwrap();
            }
        }
    }

    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
}

/// Render the companion `#[interface]` trait for a mixed class.
///
/// Emits a `hicc::import_class!` block with `#[interface]` containing
/// the pure-virtual methods, named `{ClassName}Interface`.
fn render_companion_interface(out: &mut String, class: &ClassIR) {
    let interface_name = format!("{}Interface", class.name);
    writeln!(out, "hicc::import_class! {{").unwrap();
    writeln!(out, "    #[interface]").unwrap();
    writeln!(out, "    class {} {{", interface_name).unwrap();
    for method in &class.pure_virtual_methods {
        render_method(out, method);
    }
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
}

fn render_string_slice(name: &str, values: &[&str]) -> String {
    let mut out = String::new();
    writeln!(out, "pub const {}: &[&str] = &[", name).unwrap();
    for value in values {
        writeln!(out, "    {:?},", value).unwrap();
    }
    writeln!(out, "];").unwrap();
    out
}

fn render_usize_slice(name: &str, values: &[usize]) -> String {
    let mut out = String::new();
    writeln!(out, "pub const {}: &[usize] = &[", name).unwrap();
    for value in values {
        writeln!(out, "    {},", value).unwrap();
    }
    writeln!(out, "];").unwrap();
    out
}

fn render_pair_slice(name: &str, values: &[(&str, &str)]) -> String {
    let mut out = String::new();
    writeln!(out, "pub const {}: &[(&str, &str)] = &[", name).unwrap();
    for (left, right) in values {
        writeln!(out, "    ({:?}, {:?}),", left, right).unwrap();
    }
    writeln!(out, "];").unwrap();
    out
}

fn render_method(out: &mut String, func: &FunctionIR) {
    // Build the method signature for #[cpp(method = "...")]
    // Use `func.cpp_signature` which contains fully qualified C++ types, then
    // replace the fully-qualified method name with the bare method name so that
    // hicc's attribute parser gets a valid member-function signature.
    //
    // Example: `cpp_signature` = "rapidjson::internal::DiyFp rapidjson::internal::DiyFp::Normalize() const"
    //  → `method_sig`           = "rapidjson::internal::DiyFp Normalize() const"
    let method_sig = func.cpp_signature.replacen(
        &format!(" {}", func.qualified_name),
        &format!(" {}", func.name),
        1,
    );

    writeln!(out, "        #[cpp(method = \"{method_sig}\")]").unwrap();

    // Build the Rust method signature.
    let self_arg = if func.is_static {
        // Static methods don't take self.
        String::new()
    } else if func.is_const {
        "&self, ".to_string()
    } else if func.is_rvalue {
        // Rvalue-ref (&&) qualified methods consume the object → `self`.
        "self, ".to_string()
    } else {
        "&mut self, ".to_string()
    };

    let ret = render_return_type(&func.rust_return_type);

    if func.is_variadic {
        // C-style variadic method: emit `unsafe fn name(self, fixed_params, ...) -> T`
        let self_str = self_arg.trim_end_matches(", ");
        let variadic_params = if func.params.is_empty() {
            if self_str.is_empty() {
                "...".to_string()
            } else {
                format!("{}, ...", self_str)
            }
        } else {
            let fixed: Vec<String> = func
                .params
                .iter()
                .map(|p| format!("{}: {}", sanitize_ident(&p.name), p.rust_type))
                .collect();
            if self_str.is_empty() {
                format!("{}, ...", fixed.join(", "))
            } else {
                format!("{}, {}, ...", self_str, fixed.join(", "))
            }
        };
        writeln!(
            out,
            "        unsafe fn {}({}){};",
            func.rust_name, variadic_params, ret
        )
        .unwrap();
    } else {
        let rust_params = render_rust_params(&func.params, &self_arg);
        writeln!(out, "        fn {}{}{};", func.rust_name, rust_params, ret).unwrap();
    }
}

fn render_import_lib(out: &mut String, decls: &ExtractedDecls, link_name: &str) {
    // Determine whether we need hicc/std/memory.hpp for @make_proxy.
    let has_abstract = decls.classes.iter().any(|c| c.is_abstract);
    let has_mixed = decls.classes.iter().any(|c| c.has_pure_virtual);

    writeln!(out, "hicc::import_lib! {{").unwrap();
    writeln!(out, "    #![link_name = \"{link_name}\"]").unwrap();
    writeln!(out).unwrap();

    // Forward-declare every concrete class used so hicc knows they are C++ types.
    for class in &decls.classes {
        if !class.is_abstract {
            let rust_name = class
                .canonical_name
                .as_deref()
                .unwrap_or(class.name.as_str());
            writeln!(out, "    class {};", rust_name).unwrap();
        }
    }

    // Collect all static methods as free functions.
    let static_methods: Vec<&FunctionIR> = decls
        .classes
        .iter()
        .flat_map(|c| c.methods.iter())
        .filter(|m| m.is_static)
        .collect();

    let has_content = !decls.functions.is_empty()
        || !static_methods.is_empty()
        || has_abstract
        || has_mixed
        || !decls.globals.is_empty();
    // Only add a blank separator if at least one concrete (non-abstract) class
    // forward-declaration was actually written above.
    let has_concrete_classes = decls.classes.iter().any(|c| !c.is_abstract);
    if has_content && has_concrete_classes {
        writeln!(out).unwrap();
    }

    // Free functions.
    for func in &decls.functions {
        render_free_function(out, func);
        writeln!(out).unwrap();
    }

    // Static class methods exposed as free functions.
    for func in static_methods {
        // Use class_name::rust_name as the Rust fn name.
        let prefixed = if let Some(ref cls) = func.class_name {
            format!("{}_{}", crate::ast::to_snake_case(cls), func.rust_name)
        } else {
            func.rust_name.clone()
        };
        render_free_function_with_name(out, func, &prefixed);
        writeln!(out).unwrap();
    }

    // @make_proxy bindings for fully-abstract classes.
    //
    // hicc's @make_proxy<X>() requires X to be a CONCRETE class with a
    // registered MethodsType.  For a fully-abstract C++ class (all methods
    // pure-virtual), we can only generate a proxy if there is a concrete
    // non-abstract class in the same declarations that has the abstract class
    // in its inheritance chain (bases list).  Without such a concrete
    // implementor the @make_proxy would reference a void MethodsType and the
    // C++ compiler would reject it.
    for class in &decls.classes {
        if !class.is_abstract {
            continue;
        }
        let rust_name = class
            .canonical_name
            .as_deref()
            .unwrap_or(class.name.as_str());

        // Find a concrete (non-abstract) class that directly inherits from
        // this abstract class.  That concrete class becomes the proxy type.
        let concrete_implementor = decls.classes.iter().find(|c| {
            !c.is_abstract
                && c.bases.iter().any(|b| {
                    b == class.name.as_str() || b == rust_name || b == class.qualified_name.as_str()
                })
        });

        let Some(impl_class) = concrete_implementor else {
            // No concrete implementor found → emit a commented-out
            // @make_proxy skeleton so users know how to wire it up once
            // they provide a concrete C++ implementor in scope.
            let snake = crate::ast::to_snake_case(rust_name);
            let proxy_fn = format!("new_{snake}_proxy");
            writeln!(
                out,
                "    // @make_proxy skeleton for `{rust_name}` — uncomment and replace"
            )
            .unwrap();
            writeln!(
                out,
                "    // `YourConcreteImpl` with a concrete C++ class that derives from it."
            )
            .unwrap();
            writeln!(
                out,
                "    // #[cpp(func = \"YourConcreteImpl @make_proxy<YourConcreteImpl>()\")]"
            )
            .unwrap();
            writeln!(out, "    // #[interface(name = \"{rust_name}\")]").unwrap();
            writeln!(
                out,
                "    // fn {proxy_fn}(intf: hicc::Interface<YourConcreteImpl>) -> YourConcreteImpl;"
            )
            .unwrap();
            writeln!(out).unwrap();
            continue;
        };

        let impl_rust_name = impl_class
            .canonical_name
            .as_deref()
            .unwrap_or(impl_class.name.as_str());
        let impl_cpp_name = &impl_class.qualified_name;
        let snake = crate::ast::to_snake_case(rust_name);
        let proxy_fn = format!("new_{}_proxy", snake);
        // Use the fully-qualified C++ name of the concrete implementor so
        // hicc finds the registered MethodsType.
        writeln!(
            out,
            "    #[cpp(func = \"{cpp} @make_proxy<{cpp}>()\")]",
            cpp = impl_cpp_name
        )
        .unwrap();
        writeln!(out, "    #[interface(name = \"{}\")]", rust_name).unwrap();
        writeln!(
            out,
            "    fn {}(intf: hicc::Interface<{}>) -> {};",
            proxy_fn, impl_rust_name, impl_rust_name
        )
        .unwrap();
        writeln!(out).unwrap();
    }

    // @make_proxy bindings for companion interfaces of mixed classes.
    //
    // For a mixed C++ class (some concrete + some pure-virtual methods), hicc
    // exposes the concrete part via `#[cpp(class = "...")]` and the
    // pure-virtual part via a synthetic companion `#[interface]` named
    // `{ClassName}Interface`.  The proxy uses the CONCRETE class as the proxy
    // type (it already has a MethodsType registered) and the companion
    // interface as the Rust trait being proxied.
    for class in &decls.classes {
        if !class.has_pure_virtual || class.pure_virtual_methods.is_empty() {
            continue;
        }
        let interface_name = format!("{}Interface", class.name);
        let rust_class_name = class
            .canonical_name
            .as_deref()
            .unwrap_or(class.name.as_str());
        let cpp_class_name = &class.qualified_name;
        let snake = crate::ast::to_snake_case(&interface_name);
        let proxy_fn = format!("new_{}_proxy", snake);
        writeln!(
            out,
            "    #[cpp(func = \"{cpp} @make_proxy<{cpp}>()\")]",
            cpp = cpp_class_name
        )
        .unwrap();
        writeln!(out, "    #[interface(name = \"{}\")]", interface_name).unwrap();
        writeln!(
            out,
            "    fn {}(intf: hicc::Interface<{}>) -> {};",
            proxy_fn, rust_class_name, rust_class_name
        )
        .unwrap();
        writeln!(out).unwrap();
    }

    // Global variable bindings via `#[cpp(data = "...")]`.
    if !decls.globals.is_empty() {
        writeln!(out, "    // Global variable bindings.").unwrap();
        for gv in &decls.globals {
            writeln!(out, "    #[cpp(data = \"{}\")]", gv.qualified_name).unwrap();
            writeln!(out, "    fn {}() -> {};", gv.rust_name, gv.rust_type).unwrap();
            writeln!(out).unwrap();
        }
    }

    writeln!(out, "}}").unwrap();
}

fn render_free_function(out: &mut String, func: &FunctionIR) {
    render_free_function_with_name(out, func, &func.rust_name);
}

fn render_free_function_with_name(out: &mut String, func: &FunctionIR, rust_name: &str) {
    writeln!(out, "    #[cpp(func = \"{}\")]", func.cpp_signature).unwrap();
    let rust_params = render_rust_params(&func.params, "");
    let ret = render_return_type(&func.rust_return_type);
    if func.is_variadic {
        // C-style variadic function: emit `unsafe fn name(fixed_params, ...) -> T`
        let inner = if func.params.is_empty() {
            "...".to_string()
        } else {
            let fixed = render_rust_params(&func.params, "");
            // Strip outer parens and append `...`
            let inner_str = fixed.trim_start_matches('(').trim_end_matches(')');
            format!("{}, ...", inner_str)
        };
        writeln!(out, "    unsafe fn {}({}){};", rust_name, inner, ret).unwrap();
    } else {
        writeln!(out, "    fn {}{}{};", rust_name, rust_params, ret).unwrap();
    }
}

fn render_rust_params(params: &[crate::ast::ParamIR], self_prefix: &str) -> String {
    let param_strs: Vec<String> = params
        .iter()
        .map(|p| format!("{}: {}", sanitize_ident(&p.name), p.rust_type))
        .collect();

    let inner = if self_prefix.is_empty() {
        param_strs.join(", ")
    } else if param_strs.is_empty() {
        // Strip the trailing ", " from self_prefix (e.g. "&self, " → "&self").
        self_prefix.trim_end_matches(", ").to_string()
    } else {
        format!("{}{}", self_prefix, param_strs.join(", "))
    };
    format!("({})", inner)
}

fn render_return_type(rust_type: &str) -> String {
    if rust_type == "()" {
        String::new()
    } else {
        format!(" -> {}", rust_type)
    }
}

/// Ensure a parameter name is a valid Rust identifier (not a keyword).
fn sanitize_ident(name: &str) -> &str {
    match name {
        "type" | "impl" | "mod" | "use" | "pub" | "fn" | "let" | "mut" | "ref" | "move"
        | "loop" | "match" | "if" | "else" | "return" | "break" | "continue" | "where" | "for"
        | "while" | "in" | "as" | "crate" | "self" | "super" | "trait" | "struct" | "enum"
        | "union" | "extern" | "unsafe" | "async" | "await" | "dyn" | "box" | "abstract"
        | "become" | "final" | "override" | "priv" | "typeof" | "unsized" | "virtual" | "yield"
        | "do" | "try" | "macro" => "value",
        other => other,
    }
}

