/// Generate a Markdown interface-checklist report, mirroring c2rust-demo's style.
pub fn render_interface_report(decls: &ExtractedDecls, link_name: &str, header: &str) -> String {
    let mut out = String::new();

    writeln!(out, "# FFI Interface Report").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "| Field | Value |").unwrap();
    writeln!(out, "|-------|-------|").unwrap();
    writeln!(out, "| Source file | `{}` |", header).unwrap();
    writeln!(out, "| Link name | `{}` |", link_name).unwrap();
    writeln!(out, "| Free functions | {} |", decls.functions.len()).unwrap();
    writeln!(out, "| Classes | {} |", decls.classes.len()).unwrap();
    writeln!(out, "| Skipped | {} |", decls.skipped.len()).unwrap();
    writeln!(out).unwrap();

    if !decls.functions.is_empty() {
        writeln!(out, "## Free Functions\n").unwrap();
        writeln!(out, "| C++ name | Rust name | Signature |").unwrap();
        writeln!(out, "|----------|-----------|-----------|").unwrap();
        for f in &decls.functions {
            writeln!(
                out,
                "| `{}` | `{}` | `{}` |",
                f.name, f.rust_name, f.cpp_signature
            )
            .unwrap();
        }
        writeln!(out).unwrap();
    }

    for class in &decls.classes {
        writeln!(out, "## Class `{}`\n", class.qualified_name).unwrap();
        writeln!(out, "| Method | Rust name | Const | Static |").unwrap();
        writeln!(out, "|--------|-----------|-------|--------|").unwrap();
        for m in &class.methods {
            writeln!(
                out,
                "| `{}` | `{}` | {} | {} |",
                m.name, m.rust_name, m.is_const, m.is_static
            )
            .unwrap();
        }
        writeln!(out).unwrap();
    }

    if !decls.skipped.is_empty() {
        writeln!(out, "## Skipped declarations\n").unwrap();
        writeln!(out, "| AST kind | Name | Reason |").unwrap();
        writeln!(out, "|----------|------|--------|").unwrap();
        for item in &decls.skipped {
            writeln!(
                out,
                "| `{}` | `{}` | `{}` |",
                item.kind, item.name, item.reason
            )
            .unwrap();
        }
        writeln!(out).unwrap();

        // Dedicated section for operator overloads: guide users on how to
        // expose them manually via a hicc::cpp! wrapper.
        let operators: Vec<&SkippedDecl> = decls
            .skipped
            .iter()
            .filter(|s| s.reason == "operator_overload")
            .collect();
        if !operators.is_empty() {
            writeln!(out, "## Operator Overload Shim Hints\n").unwrap();
            writeln!(out, "The following C++ operators were skipped. hicc does not support\n\
                           operator overloads directly. Expose them by writing a named C++\n\
                           wrapper function inside a `hicc::cpp!` block and then binding\n\
                           that wrapper via `#[cpp(func = \"...\")]` in `import_lib!`.\n")
                .unwrap();
            writeln!(out, "| Skipped operator | Suggested approach |").unwrap();
            writeln!(out, "|-----------------|-------------------|").unwrap();
            for op in &operators {
                let suggestion = operator_shim_suggestion(&op.name);
                writeln!(out, "| `{}` | {} |", op.name, suggestion).unwrap();
            }
            writeln!(out).unwrap();
            writeln!(
                out,
                "**Example** – wrapping `operator[]` as a named function:\n\
                 \n\
                 ```rust\n\
                 hicc::cpp! {{\n\
                 //   static ReturnType get_at(MyClass& obj, IndexType idx) {{ return obj[idx]; }}\n\
                 }}\n\
                 hicc::import_lib! {{\n\
                 //   #![link_name = \"mylib\"]\n\
                 //   class MyClass;\n\
                 //   #[cpp(func = \"ReturnType get_at(MyClass&, IndexType)\")]\n\
                 //   fn get_at(obj: &mut MyClass, idx: IndexType) -> ReturnType;\n\
                 }}\n\
                 ```"
            )
            .unwrap();
            writeln!(out).unwrap();
        }
    }

    out
}

