/// Generate a Markdown interface-checklist report, mirroring c2rust-demo's style.
pub fn render_interface_report(decls: &ExtractedDecls, link_name: &str, header: &str) -> String {
    let mut out = String::new();

    let total_ctors: usize = decls.classes.iter().map(|c| c.ctors.len()).sum();
    let abstract_classes: usize = decls.classes.iter().filter(|c| c.is_abstract).count();
    let mixed_classes: usize = decls.classes.iter().filter(|c| c.has_pure_virtual).count();

    writeln!(out, "# FFI Interface Report").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "| Field | Value |").unwrap();
    writeln!(out, "|-------|-------|").unwrap();
    writeln!(out, "| Source file | `{}` |", header).unwrap();
    writeln!(out, "| Link name | `{}` |", link_name).unwrap();
    writeln!(out, "| Free functions | {} |", decls.functions.len()).unwrap();
    writeln!(out, "| Classes | {} |", decls.classes.len()).unwrap();
    writeln!(
        out,
        "| Abstract classes (interfaces) | {} |",
        abstract_classes
    )
    .unwrap();
    if mixed_classes > 0 {
        writeln!(
            out,
            "| Mixed classes (companion interface) | {} |",
            mixed_classes
        )
        .unwrap();
    }
    writeln!(out, "| Extracted constructors | {} |", total_ctors).unwrap();
    writeln!(
        out,
        "| Global variables | {} |",
        decls
            .globals
            .iter()
            .filter(|g| g.class_name.is_none())
            .count()
    )
    .unwrap();
    writeln!(
        out,
        "| Static data members | {} |",
        decls
            .globals
            .iter()
            .filter(|g| g.class_name.is_some())
            .count()
    )
    .unwrap();
    writeln!(out, "| Enums | {} |", decls.enums.len()).unwrap();
    writeln!(out, "| Type aliases | {} |", decls.aliases.len()).unwrap();
    writeln!(out, "| Skipped | {} |", decls.skipped.len()).unwrap();
    writeln!(out).unwrap();

    if !decls.functions.is_empty() {
        writeln!(out, "## Free Functions\n").unwrap();
        writeln!(out, "| C++ name | Rust name | Signature |").unwrap();
        writeln!(out, "|----------|-----------|-----------|").unwrap();
        for f in &decls.functions {
            writeln!(
                out,
                "| `{}` | `{}` | `{}` |",
                f.name, f.rust_name, f.cpp_signature
            )
            .unwrap();
        }
        writeln!(out).unwrap();
    }

    for class in &decls.classes {
        // Header: show canonical name (alias) for template specialisations.
        let display_name = class
            .canonical_name
            .as_deref()
            .unwrap_or(class.name.as_str());
        if class.is_abstract {
            writeln!(out, "## Class `{}` `[interface]`\n", display_name).unwrap();
        } else if class.has_pure_virtual {
            writeln!(
                out,
                "## Class `{}` (mixed; companion interface `{}Interface`)\n",
                display_name, class.name
            )
            .unwrap();
        } else {
            writeln!(out, "## Class `{}`\n", display_name).unwrap();
        }
        if class.is_template_specialization {
            writeln!(
                out,
                "_Template specialisation of `{}`._\n",
                class.qualified_name
            )
            .unwrap();
        }

        // Constructors
        if !class.ctors.is_empty() {
            writeln!(out, "### Constructors\n").unwrap();
            writeln!(out, "| C++ signature | hicc role |").unwrap();
            writeln!(out, "|---------------|-----------|").unwrap();
            for (i, ctor) in class.ctors.iter().enumerate() {
                let role = if i == 0 {
                    "primary (`ctor = \"...\"`)"
                } else {
                    "extra factory fn"
                };
                writeln!(out, "| `{}` | {} |", ctor.cpp_signature, role).unwrap();
            }
            writeln!(out).unwrap();
        }

        // Base classes
        if !class.bases.is_empty() {
            writeln!(
                out,
                "**Bases:** {}\n",
                class
                    .bases
                    .iter()
                    .map(|b| format!("`{}`", b))
                    .collect::<Vec<_>>()
                    .join(", ")
            )
            .unwrap();
        }

        // Virtual bases that were skipped.
        if !class.skipped_virtual_bases.is_empty() {
            writeln!(
                out,
                "> ⚠️ **Virtual bases (skipped — hicc does not support virtual inheritance):** {}\n",
                class
                    .skipped_virtual_bases
                    .iter()
                    .map(|b| format!("`{}`", b))
                    .collect::<Vec<_>>()
                    .join(", ")
            )
            .unwrap();
        }

        if !class.methods.is_empty() {
            writeln!(out, "| Method | Rust name | Const | Static |").unwrap();
            writeln!(out, "|--------|-----------|-------|--------|").unwrap();
            for m in &class.methods {
                writeln!(
                    out,
                    "| `{}` | `{}` | {} | {} |",
                    m.name, m.rust_name, m.is_const, m.is_static
                )
                .unwrap();
            }
            writeln!(out).unwrap();
        }

        // Instance fields.
        if !class.fields.is_empty() {
            writeln!(out, "### Instance Fields\n").unwrap();
            writeln!(
                out,
                "| Field | C++ type | Rust type | Const | Read accessor | Write accessor |"
            )
            .unwrap();
            writeln!(
                out,
                "|-------|----------|-----------|-------|---------------|----------------|"
            )
            .unwrap();
            for f in &class.fields {
                let write_acc = if f.is_const {
                    "—".to_string()
                } else {
                    format!("`get_{}_mut(&mut self)`", f.rust_name)
                };
                writeln!(
                    out,
                    "| `{}` | `{}` | `{}` | {} | `get_{}(&self)` | {} |",
                    f.name, f.cpp_type, f.rust_type, f.is_const, f.rust_name, write_acc
                )
                .unwrap();
            }
            writeln!(out).unwrap();
        }

        // Pure-virtual methods (companion interface) for mixed classes.
        if !class.pure_virtual_methods.is_empty() {
            writeln!(out, "### Companion Interface (`{}Interface`)\n", class.name).unwrap();
            writeln!(out, "| Method | Rust name | Const |").unwrap();
            writeln!(out, "|--------|-----------|-------|").unwrap();
            for m in &class.pure_virtual_methods {
                writeln!(out, "| `{}` | `{}` | {} |", m.name, m.rust_name, m.is_const).unwrap();
            }
            writeln!(out).unwrap();
        }

        // @make_proxy hint for abstract classes
        if class.is_abstract {
            let rust_name = class
                .canonical_name
                .as_deref()
                .unwrap_or(class.name.as_str());
            let snake = crate::ast::to_snake_case(rust_name);
            writeln!(out, "### @make_proxy Binding\n").unwrap();
            writeln!(
                out,
                "Use `new_{snake}_proxy(impl_struct)` to wrap a Rust struct as a `{}` instance.\n",
                rust_name
            )
            .unwrap();
        }
    }

    // Global variables section
    if !decls.globals.is_empty() {
        writeln!(out, "## Global Variables\n").unwrap();
        writeln!(
            out,
            "| C++ name | Rust fn name | Qualified name | C++ type | Rust type | Const | Owner |"
        )
        .unwrap();
        writeln!(
            out,
            "|----------|--------------|----------------|----------|-----------|-------|-------|"
        )
        .unwrap();
        for gv in &decls.globals {
            let owner = gv.class_name.as_deref().unwrap_or("(global)");
            writeln!(
                out,
                "| `{}` | `{}` | `{}` | `{}` | `{}` | {} | `{}` |",
                gv.name,
                gv.rust_name,
                gv.qualified_name,
                gv.cpp_type,
                gv.rust_type,
                gv.is_const,
                owner
            )
            .unwrap();
        }
        writeln!(out).unwrap();
    }

    // Enum definitions section
    if !decls.enums.is_empty() {
        writeln!(out, "## Enums\n").unwrap();
        for enum_ir in &decls.enums {
            let kind = if enum_ir.is_class {
                "enum class"
            } else {
                "enum"
            };
            writeln!(
                out,
                "### `{} {}` (`{}`)\n",
                kind, enum_ir.name, enum_ir.qualified_name
            )
            .unwrap();
            writeln!(out, "| Variant | Value |").unwrap();
            writeln!(out, "|---------|-------|").unwrap();
            for v in &enum_ir.variants {
                let val = v
                    .value
                    .map(|n| n.to_string())
                    .unwrap_or_else(|| "(implicit)".to_string());
                writeln!(out, "| `{}` | `{}` |", v.name, val).unwrap();
            }
            writeln!(out).unwrap();
        }
    }

    // Type aliases section
    if !decls.aliases.is_empty() {
        writeln!(out, "## Type Aliases\n").unwrap();
        writeln!(out, "| C++ alias | Underlying C++ type | Rust type |").unwrap();
        writeln!(out, "|-----------|---------------------|-----------|").unwrap();
        for alias in &decls.aliases {
            writeln!(
                out,
                "| `{}` | `{}` | `{}` |",
                alias.name, alias.aliased_cpp_type, alias.aliased_rust_type
            )
            .unwrap();
        }
        writeln!(out).unwrap();
    }

    if !decls.skipped.is_empty() {
        writeln!(out, "## Skipped declarations\n").unwrap();

        // 主线六: Group skipped items by category.
        let tool_conservative: Vec<&SkippedDecl> = decls
            .skipped
            .iter()
            .filter(|s| s.category == SkipCategory::ToolConservative)
            .collect();
        let hicc_limitation: Vec<&SkippedDecl> = decls
            .skipped
            .iter()
            .filter(|s| s.category == SkipCategory::HiccLimitation)
            .collect();

        if !tool_conservative.is_empty() {
            writeln!(out, "### Tool-conservative skips (`tool_conservative`)\n").unwrap();
            writeln!(out, "_These were skipped due to conservative tooling decisions. Future versions of cpp2rust-demo may extract them automatically._\n").unwrap();
            writeln!(out, "| AST kind | Name | Reason |").unwrap();
            writeln!(out, "|----------|------|--------|").unwrap();
            for item in &tool_conservative {
                writeln!(
                    out,
                    "| `{}` | `{}` | `{}` |",
                    item.kind, item.name, item.reason
                )
                .unwrap();
            }
            writeln!(out).unwrap();

            // Emit alias suggestions for template_decl skips.
            let template_skips: Vec<&SkippedDecl> = tool_conservative
                .iter()
                .copied()
                .filter(|s| s.reason == "template_decl" && s.suggested_alias.is_some())
                .collect();
            if !template_skips.is_empty() {
                writeln!(
                    out,
                    "#### Alias suggestions to unlock template extraction\n"
                )
                .unwrap();
                writeln!(out, "Add the following `using` declarations to your C++ header and re-run `cpp2rust-demo init` to unlock extraction:\n").unwrap();
                writeln!(out, "```cpp").unwrap();
                for item in &template_skips {
                    if let Some(ref hint) = item.suggested_alias {
                        writeln!(out, "// Template: {}", item.name).unwrap();
                        writeln!(out, "{}", hint).unwrap();
                    }
                }
                writeln!(out, "```\n").unwrap();
                writeln!(out, "Run `cpp2rust-demo suggest-aliases --feature <name>` for a formatted summary.\n").unwrap();
            }
        }

        if !hicc_limitation.is_empty() {
            writeln!(out, "### hicc limitations (`hicc_limitation`)\n").unwrap();
            writeln!(
                out,
                "_These require a hand-written C++ shim or are not supported by hicc._\n"
            )
            .unwrap();
            writeln!(out, "| AST kind | Name | Reason |").unwrap();
            writeln!(out, "|----------|------|--------|").unwrap();
            for item in &hicc_limitation {
                writeln!(
                    out,
                    "| `{}` | `{}` | `{}` |",
                    item.kind, item.name, item.reason
                )
                .unwrap();
            }
            writeln!(out).unwrap();
        }

        // Shim suggestions for std::string / std::function / function-pointer skips.
        let shim_suggestions: Vec<&SkippedDecl> = decls
            .skipped
            .iter()
            .filter(|s| s.suggested_shim.is_some())
            .collect();
        if !shim_suggestions.is_empty() {
            writeln!(out, "## Shim Suggestions for Skipped Functions\n").unwrap();
            writeln!(out, "_The following functions were skipped because they use types that hicc cannot pass through the ABI boundary directly (`std::string`, `std::function`, or function pointers). Copy the suggested shim or interface prototypes into your C++ header or a `hicc::cpp!` block, then add `#[cpp(func = \"...\")]` bindings in `import_lib!`._\n").unwrap();
            for item in &shim_suggestions {
                writeln!(out, "**`{}`** (`{}`):\n", item.name, item.reason).unwrap();
                if let Some(ref shim) = item.suggested_shim {
                    writeln!(out, "```cpp\n{}\n```\n", shim).unwrap();
                }
            }
        }

        // Variadic functions that were extracted with a `...` parameter.
        let variadic_fns: Vec<&crate::ast::FunctionIR> =
            decls.functions.iter().filter(|f| f.is_variadic).collect();
        let variadic_methods: Vec<(&crate::ast::ClassIR, &crate::ast::FunctionIR)> = decls
            .classes
            .iter()
            .flat_map(|c| {
                c.methods
                    .iter()
                    .filter(|m| m.is_variadic)
                    .map(move |m| (c, m))
            })
            .collect();
        if !variadic_fns.is_empty() || !variadic_methods.is_empty() {
            writeln!(out, "## Variadic Functions (`va_list` last-param)\n").unwrap();
            writeln!(out, "_These functions have a `va_list` last parameter and are bound as `unsafe fn ... (fixed_params, ...) -> T`. Call them with a Rust format macro or pass a `VaList` obtained from another variadic function._\n").unwrap();
            for f in &variadic_fns {
                writeln!(
                    out,
                    "- `{}` → `unsafe fn {}(...)`",
                    f.qualified_name, f.rust_name
                )
                .unwrap();
            }
            for (cls, m) in &variadic_methods {
                writeln!(
                    out,
                    "- `{}::{}` → `unsafe fn {}(...)`",
                    cls.qualified_name, m.name, m.rust_name
                )
                .unwrap();
            }
            writeln!(out).unwrap();
        }

        // Dynamic cast skeleton hint.
        let inherited_classes: Vec<&crate::ast::ClassIR> = decls
            .classes
            .iter()
            .filter(|c| !c.bases.is_empty())
            .collect();
        if !inherited_classes.is_empty() {
            writeln!(out, "## @dynamic_cast Bindings\n").unwrap();
            writeln!(out, "_The following classes have public base classes. Active `@dynamic_cast` bindings have been appended to `<stem>.rs` (see the `// --- dynamic cast starters ---` section)._\n").unwrap();
            for cls in &inherited_classes {
                let rust_name = cls.canonical_name.as_deref().unwrap_or(cls.name.as_str());
                writeln!(
                    out,
                    "- **`{}`** inherits from: {}",
                    rust_name,
                    cls.bases
                        .iter()
                        .map(|b| format!("`{}`", b))
                        .collect::<Vec<_>>()
                        .join(", ")
                )
                .unwrap();
            }
            writeln!(out).unwrap();
        }

        // Dedicated section for operator overloads: guide users on how to
        // expose them via operator shims.
        let operators: Vec<&SkippedDecl> = decls
            .skipped
            .iter()
            .filter(|s| s.reason == "operator_overload")
            .collect();
        if !operators.is_empty() {
            writeln!(out, "## Operator Overload Shim Hints\n").unwrap();
            out.push_str(
                "The following C++ operators were skipped. \
                 hicc does not support operator overloads directly. \
                 Auto-generated C++ shims have been written to `meta/<group>/operator_shims.hpp` \
                 and the corresponding Rust bindings are active in `<stem>.rs`.\n\n",
            );
            writeln!(out, "| Skipped operator | Suggested shim name |").unwrap();
            writeln!(out, "|-----------------|---------------------|").unwrap();
            for op in &operators {
                let shim = decls
                    .operator_shims
                    .iter()
                    .find(|s| {
                        s.operator_name == op.name.rsplit("::").next().unwrap_or(&op.name)
                            || op.name.contains(&s.operator_name)
                    })
                    .map(|s| s.shim_name.as_str())
                    .unwrap_or("(see operator_shims.hpp)");
                writeln!(out, "| `{}` | `{}` |", op.name, shim).unwrap();
            }
            writeln!(out).unwrap();
            out.push_str(
                "See `meta/<group>/operator_shims.hpp` for the generated shim signatures.\n\n",
            );
        }
    }

    // Placement-new skeletons section (P4).
    {
        let classes_with_ctors: Vec<&crate::ast::ClassIR> = decls
            .classes
            .iter()
            .filter(|c| !c.is_abstract && !c.ctors.is_empty())
            .collect();
        if !classes_with_ctors.is_empty() {
            writeln!(out, "## Placement-New Bindings (P4)\n").unwrap();
            writeln!(out, "_The following concrete classes have extracted constructors. Active placement-new bindings have been appended to `<stem>.rs` (see the `// --- placement-new starters ---` section) to build C++ objects in Rust-managed memory._\n").unwrap();
            for cls in &classes_with_ctors {
                let rust_name = cls.canonical_name.as_deref().unwrap_or(cls.name.as_str());
                let ctor_sigs: Vec<String> = cls
                    .ctors
                    .iter()
                    .map(|c| format!("`{}`", c.cpp_signature))
                    .collect();
                writeln!(
                    out,
                    "- **`{rust_name}`** — constructors: {}",
                    ctor_sigs.join(", ")
                )
                .unwrap();
            }
            writeln!(out).unwrap();
        }
    }

    // RustAny suggestions section (P4).
    {
        let stl_types: std::collections::BTreeSet<String> = decls
            .skipped
            .iter()
            .filter_map(|s| s.stl_container_type.clone())
            .collect();
        if !stl_types.is_empty() {
            writeln!(
                out,
                "## `hicc::RustAny` Suggestions for STL Containers (P4)\n"
            )
            .unwrap();
            writeln!(out, "_The following STL container types were encountered in skipped declarations. `hicc::RustAny<T>` / `hicc-std` suggestions have been appended to `<stem>.rs`._\n").unwrap();
            for ct in &stl_types {
                writeln!(out, "- `{ct}`").unwrap();
            }
            writeln!(out).unwrap();
        }
    }

    out
}

/// Render the C++ shim header for operator overloads (主线四) and
/// std::string / std::function shim suggestions (P2).
///
/// Each operator that was skipped during extraction gets a named wrapper
/// function written into `operator_shims.hpp`.  Users can `#include` this
/// file in their `hicc::cpp!` block and then bind the wrappers via
/// `#[cpp(func = "...")]` in `import_lib!`.
///
/// Additionally, any `suggested_shim` text from skipped declarations is
/// appended to the file as commented-out prototype suggestions.
pub fn render_operator_shims_hpp(
    shims: &[OperatorShimIR],
    skipped: &[SkippedDecl],
    middleware_basename: &str,
) -> String {
    let string_shims: Vec<&SkippedDecl> = skipped
        .iter()
        .filter(|s| s.suggested_shim.is_some())
        .collect();

    if shims.is_empty() && string_shims.is_empty() {
        return String::new();
    }

    let guard = "OPERATOR_SHIMS_HPP";
    let mut out = String::new();
    writeln!(out, "// Auto-generated operator shims by cpp2rust-demo.").unwrap();
    writeln!(
        out,
        "// Include this file in your hicc::cpp! block, then bind"
    )
    .unwrap();
    writeln!(
        out,
        "// the functions below via #[cpp(func = \"...\")] in import_lib!."
    )
    .unwrap();
    writeln!(out, "#pragma once").unwrap();
    writeln!(out, "#ifndef {}", guard).unwrap();
    writeln!(out, "#define {}", guard).unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#include \"{}\"", middleware_basename).unwrap();
    writeln!(out).unwrap();

    for shim in shims {
        let class_ref = shim.qualified_class.as_deref().unwrap_or("");
        let self_param = if let Some(ref _cls) = shim.class_name {
            if !class_ref.is_empty() {
                let const_q = if shim.is_const { "const " } else { "" };
                format!("{}{} &self", const_q, class_ref)
            } else {
                String::new()
            }
        } else {
            String::new()
        };

        let extra_params: Vec<String> = shim
            .params
            .iter()
            .map(|p| format!("{} {}", p.cpp_type, p.name))
            .collect();

        let all_params: Vec<String> = if self_param.is_empty() {
            extra_params
        } else {
            std::iter::once(self_param).chain(extra_params).collect()
        };

        let ret = if shim.return_cpp_type.is_empty() || shim.return_cpp_type == "()" {
            "void".to_string()
        } else {
            shim.return_cpp_type.clone()
        };

        let call_self = if shim.class_name.is_some() {
            "self"
        } else {
            ""
        };
        let call_extra: Vec<&str> = shim.params.iter().map(|p| p.name.as_str()).collect();
        let call_args = call_extra.join(", ");

        let call_expr = if let Some(ref _cls) = shim.class_name {
            match shim.operator_name.as_str() {
                "operator[]" => {
                    format!("{}[{}]", call_self, call_args)
                }
                "operator=" => {
                    format!("{} = {}", call_self, call_args)
                }
                "operator()" => {
                    format!("{}({})", call_self, call_args)
                }
                op => {
                    // Binary / comparison operators.
                    let op_token = op.strip_prefix("operator").unwrap_or(op).trim();
                    if call_args.is_empty() {
                        // Unary prefix
                        format!("{}{}", op_token, call_self)
                    } else {
                        format!("{} {} {}", call_self, op_token, call_args)
                    }
                }
            }
        } else {
            // Free-standing operator.
            let all_args: Vec<&str> = shim.params.iter().map(|p| p.name.as_str()).collect();
            let op_token = shim
                .operator_name
                .strip_prefix("operator")
                .unwrap_or(&shim.operator_name)
                .trim();
            if all_args.len() == 2 {
                format!("{} {} {}", all_args[0], op_token, all_args[1])
            } else {
                format!("{}({})", shim.operator_name, all_args.join(", "))
            }
        };

        writeln!(out, "/// Shim for `{}`", shim.operator_name).unwrap();
        writeln!(
            out,
            "static inline {} {}({}) {{ return {}; }}",
            ret,
            shim.shim_name,
            all_params.join(", "),
            call_expr
        )
        .unwrap();
        writeln!(out).unwrap();
    }

    // Append std::string / std::function shim suggestions as commented-out starters.
    if !string_shims.is_empty() {
        writeln!(
            out,
            "// ---------------------------------------------------------------------------"
        )
        .unwrap();
        writeln!(
            out,
            "// std::string / std::function shim suggestions (copy-paste starters)"
        )
        .unwrap();
        writeln!(
            out,
            "// ---------------------------------------------------------------------------"
        )
        .unwrap();
        writeln!(out).unwrap();
        for item in &string_shims {
            if let Some(ref shim_text) = item.suggested_shim {
                writeln!(out, "// Skipped function: `{}`", item.name).unwrap();
                writeln!(out, "{}", shim_text).unwrap();
            }
        }
    }

    writeln!(out, "#endif // {}", guard).unwrap();
    out
}

/// Render the Rust free-function stubs for operator shims (主线四).
///
/// These stubs are appended to `<stem>.rs` (after a `// --- operator shims ---`
/// comment separator).  They provide `#[cpp(func = "...")]` bindings for each
/// operator shim so that Rust can call the wrapper functions.
///
/// The bindings are emitted as **active** (uncommented) code.  The corresponding
/// C++ shim file is automatically included via `operator_shims.hpp` in the
/// `hicc::cpp!` block, so no manual activation step is required.
pub fn render_operator_shims_rs(shims: &[OperatorShimIR], link_name: &str) -> String {
    if shims.is_empty() {
        return String::new();
    }

    let mut out = String::new();
    writeln!(
        out,
        "// Auto-generated operator shim Rust bindings by cpp2rust-demo."
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "hicc::import_lib! {{").unwrap();
    writeln!(out, "    #![link_name = \"{link_name}\"]").unwrap();
    writeln!(out).unwrap();

    for shim in shims {
        let ret = if shim.return_cpp_type.is_empty() || shim.return_cpp_type == "()" {
            "()".to_string()
        } else {
            crate::ast::cpp_to_rust_type(&shim.return_cpp_type)
        };

        // Build Rust param list.
        let mut rust_params: Vec<String> = Vec::new();
        if let Some(ref cls) = shim.class_name {
            if shim.is_const {
                rust_params.push(format!("this_val: &{}", cls));
            } else {
                rust_params.push(format!("this_val: &mut {}", cls));
            }
        }
        for p in &shim.params {
            rust_params.push(format!("{}: {}", sanitize_ident(&p.name), p.rust_type));
        }
        let param_str = rust_params.join(", ");

        let ret_str = if ret == "()" {
            String::new()
        } else {
            format!(" -> {}", ret)
        };

        // Compute C++ signature for the shim function.
        let cpp_ret = if shim.return_cpp_type.is_empty() {
            "void"
        } else {
            &shim.return_cpp_type
        };
        let mut cpp_params: Vec<String> = Vec::new();
        if let Some(ref cls) = shim.qualified_class {
            let const_q = if shim.is_const { "const " } else { "" };
            cpp_params.push(format!("{}{} &", const_q, cls));
        }
        for p in &shim.params {
            cpp_params.push(p.cpp_type.clone());
        }
        let cpp_sig = format!("{} {}({})", cpp_ret, shim.shim_name, cpp_params.join(", "));

        writeln!(out, "    // Shim for `{}`", shim.operator_name).unwrap();
        writeln!(out, "    #[cpp(func = \"{}\")]", cpp_sig).unwrap();
        writeln!(
            out,
            "    fn {}({}){};\n",
            shim.shim_name, param_str, ret_str
        )
        .unwrap();
    }

    writeln!(out, "}}").unwrap();
    out
}

/// Render `@dynamic_cast` binding skeletons for classes that inherit from other classes.
///
/// For each (Base → Derived) relationship found in `decls.classes`, emits an
/// active `@dynamic_cast` import_lib! stub.  The result is appended to
/// `<stem>.rs` (after a `// --- dynamic cast starters ---` separator).
///
/// Returns an empty string when no inheritance relationships exist.
pub fn render_dynamic_casts_module(decls: &ExtractedDecls, link_name: &str) -> String {
    // Collect (base_name, derived_name) pairs from classes with non-virtual
    // public base classes.
    let cast_pairs: Vec<(String, String)> = decls
        .classes
        .iter()
        .filter(|c| !c.bases.is_empty())
        .flat_map(|c| {
            let derived_name = c
                .canonical_name
                .as_deref()
                .unwrap_or(c.name.as_str())
                .to_string();
            c.bases.iter().map(move |base| {
                let base_name = base
                    .rsplit("::")
                    .next()
                    .unwrap_or(base.as_str())
                    .to_string();
                (base_name, derived_name.clone())
            })
        })
        .collect();

    if cast_pairs.is_empty() {
        return String::new();
    }

    let mut out = String::new();
    writeln!(
        out,
        "// Auto-generated @dynamic_cast bindings by cpp2rust-demo."
    )
    .unwrap();
    writeln!(
        out,
        "// Each binding allows downcasting a Base* pointer to a Derived* at runtime."
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "hicc::import_lib! {{").unwrap();
    writeln!(out, "    #![link_name = \"{link_name}\"]").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "    // Dynamic cast bindings — downcast Base* → Derived*."
    )
    .unwrap();
    for (base_name, derived_name) in &cast_pairs {
        let snake_derived = crate::ast::to_snake_case(derived_name);
        writeln!(out).unwrap();
        writeln!(
            out,
            "    // Cast {base_name}* → {derived_name}* (returns null if types don't match)."
        )
        .unwrap();
        writeln!(
            out,
            "    #[cpp(func = \"{derived_name} @dynamic_cast<{derived_name}>({base_name} *)\")]"
        )
        .unwrap();
        writeln!(
            out,
            "    fn dynamic_cast_to_{snake_derived}(ptr: *mut {base_name}) -> *mut {derived_name};"
        )
        .unwrap();
    }
    writeln!(out, "}}").unwrap();
    out
}

/// Render placement-new binding skeletons for classes with constructors.
///
/// hicc supports constructing C++ objects directly in Rust-managed memory via
/// `@placement_new<ClassName>(args...)`.  The generated bindings are emitted as
/// active code ready for use.
///
/// Returns an empty string when no concrete class has any extracted constructors.
pub fn render_placement_new_module(decls: &ExtractedDecls, link_name: &str) -> String {
    let classes_with_ctors: Vec<&ClassIR> = decls
        .classes
        .iter()
        .filter(|c| !c.is_abstract && !c.ctors.is_empty())
        .collect();

    if classes_with_ctors.is_empty() {
        return String::new();
    }

    let mut out = String::new();
    writeln!(
        out,
        "// Auto-generated placement-new bindings by cpp2rust-demo."
    )
    .unwrap();
    writeln!(
        out,
        "// hicc supports constructing C++ objects in Rust-managed memory."
    )
    .unwrap();
    writeln!(out, "//").unwrap();
    writeln!(out, "// Usage pattern:").unwrap();
    writeln!(
        out,
        "//   let mut storage = hicc::AlignedStorage::<Foo>::new();"
    )
    .unwrap();
    writeln!(
        out,
        "//   let foo_ref = new_foo_inplace(&mut storage, ...);"
    )
    .unwrap();
    writeln!(
        out,
        "//   // `foo_ref` borrows `storage`; it is destroyed when `storage` drops."
    )
    .unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// See https://docs.rs/hicc for placement-new semantics."
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "hicc::import_lib! {{").unwrap();
    writeln!(out, "    #![link_name = \"{link_name}\"]").unwrap();

    for class in &classes_with_ctors {
        let rust_name = class
            .canonical_name
            .as_deref()
            .unwrap_or(class.name.as_str());
        let snake = crate::ast::to_snake_case(rust_name);

        writeln!(out).unwrap();
        writeln!(out, "    // Placement-new binding(s) for `{rust_name}`.").unwrap();
        let mut inplace_idx: usize = 0;
        for (i, ctor) in class.ctors.iter().enumerate() {
            // Skip the primary constructor (fewest params = index 0) when it
            // has no parameters: a 0-param ctor is already bound via
            // `ctor = "ClassName()"` in `import_class!` and does not benefit
            // from a placement-new binding.  Ctors with parameters are always
            // included — they represent meaningful construction alternatives.
            if i == 0 && ctor.params.is_empty() {
                continue;
            }
            // Build rust param list (no self; first param is the storage).
            let rust_params: Vec<String> = ctor
                .params
                .iter()
                .map(|p| format!("{}: {}", sanitize_ident(&p.name), p.rust_type))
                .collect();
            let param_str = if rust_params.is_empty() {
                String::new()
            } else {
                format!(", {}", rust_params.join(", "))
            };

            // Build the C++ param type list for @placement_new.
            let cpp_param_types: Vec<String> =
                ctor.params.iter().map(|p| p.cpp_type.clone()).collect();
            let cpp_params_str = cpp_param_types.join(", ");

            // Sequential naming: first included ctor → base name, second → _2, etc.
            let fn_name = if inplace_idx == 0 {
                format!("new_{snake}_inplace")
            } else {
                format!("new_{snake}_inplace_{}", inplace_idx + 1)
            };
            inplace_idx += 1;

            writeln!(
                out,
                "    #[cpp(func = \"{rust_name} @placement_new<{rust_name}>({cpp_params_str})\")]"
            )
            .unwrap();
            writeln!(out, "    fn {fn_name}<'a>(mem: &'a mut hicc::AlignedStorage<{rust_name}>{param_str}) -> &'a mut {rust_name};").unwrap();
        }
    }

    writeln!(out, "}}").unwrap();
    out
}

/// Render `hicc::RustAny<T>` type-mapping suggestions for STL container types
/// that were encountered and skipped during AST extraction.
///
/// When C++ functions/methods use STL container parameters (e.g.
/// `std::vector<Foo>`, `std::map<Key, Value>`), those functions are currently
/// skipped because hicc cannot pass container objects across the ABI boundary
/// directly.  This function generates hint comments appended to `<stem>.rs` suggesting
/// how to use `hicc::RustAny` or `hicc-std` equivalents to store Rust data in
/// C++ containers.
///
/// Returns an empty string when no STL container types were detected.
pub fn render_rust_any_suggestions(decls: &ExtractedDecls) -> String {
    // Collect unique STL container types from skipped declarations.
    let mut seen: std::collections::BTreeSet<String> = std::collections::BTreeSet::new();
    for s in &decls.skipped {
        if let Some(ref ct) = s.stl_container_type {
            seen.insert(ct.clone());
        }
    }

    if seen.is_empty() {
        return String::new();
    }

    let mut out = String::new();
    writeln!(
        out,
        "// ---------------------------------------------------------------------------"
    )
    .unwrap();
    writeln!(
        out,
        "// hicc::RustAny suggestions for STL container types (P4)"
    )
    .unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// The following STL container types were encountered as parameter or return"
    )
    .unwrap();
    writeln!(
        out,
        "// types in skipped declarations. They cannot be passed through the ABI"
    )
    .unwrap();
    writeln!(
        out,
        "// boundary directly, but hicc supports storing Rust data inside C++"
    )
    .unwrap();
    writeln!(out, "// containers via `hicc::RustAny<T>`.").unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// Pattern: replace `std::vector<Foo>` with `std::vector<hicc::RustAny<Foo>>`"
    )
    .unwrap();
    writeln!(
        out,
        "// on the C++ side, then use `hicc_std::Vector<Foo>` on the Rust side."
    )
    .unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// See https://docs.rs/hicc for RustAny / hicc-std container semantics."
    )
    .unwrap();
    writeln!(
        out,
        "// ---------------------------------------------------------------------------"
    )
    .unwrap();
    writeln!(out).unwrap();
    for ct in &seen {
        // Derive a friendly suggestion based on the container type.
        let suggestion = rust_any_suggestion_for(ct);
        writeln!(out, "// Detected: `{ct}`").unwrap();
        writeln!(out, "//   {suggestion}").unwrap();
    }
    out
}

/// Produce a one-line suggestion string for a given STL container type.
fn rust_any_suggestion_for(ct: &str) -> String {
    // Match common containers and produce hicc-std equivalents.
    let trimmed = ct.trim();
    if trimmed.contains("std::vector<") {
        let inner = extract_template_arg(trimmed);
        if let Some(t) = inner {
            return format!(
                "→ use `hicc_std::Vector<{t}>` (Rust) / `std::vector<hicc::RustAny<{t}>>` (C++)"
            );
        }
    }
    if trimmed.contains("std::map<") {
        let inner = extract_template_arg(trimmed);
        if let Some(t) = inner {
            return format!(
                "→ use `hicc_std::Map<{t}>` (Rust) / `std::map<Key, hicc::RustAny<Val>>` (C++)"
            );
        }
    }
    if trimmed.contains("std::unordered_map<") {
        let inner = extract_template_arg(trimmed);
        if let Some(t) = inner {
            return format!(
                "→ use `hicc_std::HashMap<{t}>` (Rust) / `std::unordered_map<Key, hicc::RustAny<Val>>` (C++)"
            );
        }
    }
    if trimmed.contains("std::set<") || trimmed.contains("std::unordered_set<") {
        return "→ store Rust keys via `hicc::RustKey<T>` / `hicc::RustHashKey<T>` in the set"
            .to_string();
    }
    format!("→ consider wrapping values with `hicc::RustAny<T>` to store Rust data in `{trimmed}`")
}

/// Extract the first template argument from a type string like `std::vector<int>`.
///
/// Returns `None` when no angle-bracket pair is found or the string is malformed.
fn extract_template_arg(full: &str) -> Option<&str> {
    let open = full.find('<')?;
    let close = full.rfind('>')?;
    if close <= open {
        return None;
    }
    let inner = full[open + 1..close].trim();
    if inner.is_empty() {
        return None;
    }
    Some(inner)
}

